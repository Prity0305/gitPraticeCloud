package swappingAndSortingNumber;

public class WithUsingThreeVariable {

	public static void main(String[] args) {
		int a = 99;
		int b = 98;
		int c = 0;	
		System.out.println("A: "+ a +", B: "+b);
		c = a;
		a = b;
		b = c;
		System.out.println("A: "+ a +", B: "+b);
		
		
	}

}
